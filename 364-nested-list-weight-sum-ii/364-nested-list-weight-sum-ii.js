/**
 * @param {NestedInteger[]} nestedList
 * @return {number}
 */
var depthSumInverse = function(nestedList) {
//     weight = maxDepth - depth + 1
//     maxDepth = most nested integer
//     recursion?
//     base cases => if empty, not valid
    
    let sum = 0;
    let levelSum = 0;
    let queue = [...nestedList];
    
    while (queue.length) {
        const len = queue.length
        for (let i = 0; i < len; i++) {
            const ni = queue.pop();
            if (ni.isInteger()) {
                levelSum += ni.getInteger();
            } else {
                queue.unshift(...ni.getList());
            }
        }
        sum += levelSum;
    }
    return sum;
};